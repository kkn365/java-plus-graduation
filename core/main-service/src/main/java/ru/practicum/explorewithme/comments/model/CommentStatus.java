package ru.practicum.explorewithme.comments.model;

/**
 * Статусы комментариев.
 * <p>
 * Описывает возможные состояния комментария в системе:
 * - PENDING: ожидает модерации
 * - APPROVED: опубликован
 * - REJECTED: отклонён
 */
public enum CommentStatus {
    /**
     * Комментарий создан и ожидает проверки модератором.
     */
    PENDING,

    /**
     * Комментарий одобрен и доступен для просмотра.
     */
    APPROVED,

    /**
     * Комментарий отклонён модератором и не будет опубликован.
     */
    REJECTED
}